/**
 * Enqueue front-end css and js.
 **/
function hpo_enqueue_frontend_assets() {

	// Register main theme stylesheet
	$theme = wp_get_theme( 'HighPoint-Orlando-Theme' );
	$theme_version = ( $theme instanceof WP_Theme ) ? $theme->get( 'Version' ) : false;

	wp_enqueue_style( 'fonts', 'https://fonts.googleapis.com/css?family=Montserrat|Open+Sans+Condensed:300|Oswald', null, $theme_version );
	wp_enqueue_style( 'style', HPO_THEME_CSS_URL . '/style.min.css', null, $theme_version );

	wp_enqueue_script( 'wp-a11y' );
	wp_enqueue_script( 'tether', 'https://cdnjs.cloudflare.com/ajax/libs/tether/1.4.0/js/tether.min.js', null, null, true );
	wp_enqueue_script( 'script', HPO_THEME_JS_URL . '/script.min.js', array( 'jquery', 'tether' ), $theme_version, true );

	// Add localized script variables to the document
	$site_url = parse_url( get_site_url() );
	wp_localize_script( 'script', 'hpo', array(
		'domain' => $site_url['host']
	) );
}

add_action( 'wp_enqueue_scripts', 'hpo_enqueue_frontend_assets' );


/**
 * De-register and re-register a newer version of jquery early in the
 * document head.
 *
 * @author Jo Dickson
 * @since 0.2.5
 */
function hpo_enqueue_jquery() {
	// Deregister jquery and re-register newer version in the document head.
	wp_deregister_script( 'jquery' );
	wp_register_script( 'jquery', 'https://ajax.googleapis.com/ajax/libs/jquery/3.2.1/jquery.min.js', null, null, false );
	wp_enqueue_script( 'jquery' );
}

add_action( 'wp_enqueue_scripts', 'hpo_enqueue_jquery', 1 );


